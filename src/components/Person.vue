<template>
  <div class="person">
    <h2>当前求和为:{{ sum }}</h2>
    <button @click="add">点我sum+1</button>
  </div>
</template>

<script lang="ts" setup name="Person">
  import { ref, onBeforeMount, onMounted, onBeforeUpdate, onUpdated, onBeforeUnmount, onUnmounted } from 'vue'

  // data
  let sum = ref(0)

  // methods
  function add() {
    sum.value += 1
  }

  // create
  console.log('创建')

  // mount
  onBeforeMount(() => {
    console.log('挂载前')
  })

  onMounted(() => {
    console.log('子----挂载完毕')
  })

  // update
  onBeforeUpdate(() => {
    console.log('更新前')
  })

  onUpdated(() => {
    console.log('更新完毕')
  })

  // unmount
  onBeforeUnmount(()=>{
    console.log('卸载前')
  })

  onUnmounted(()=>{
    console.log('卸载后')
  })

</script>

